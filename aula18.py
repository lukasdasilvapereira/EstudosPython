# > maior
# >= maior ou igual
# == igual
# != diferente
# <= menor ou igual

maior = 2 > 1
maior_ou_igual = 5 >= 5
menor = 3 < 1
menor_ou_igual = 5 <= 10
igual = 10 == 100
diferente = 10 != 20

print(maior)
print(maior_ou_igual)
print(menor)
print(diferente)
print(igual)
print(menor_ou_igual)

